// Comparison Operators
// ==, ===, !=, !==, >, <, >=, <= ==

let j = 10;
let k = 20;
console.log("Equal: " + (j == k));
console.log("Strict Equal: " + (j === k));
console.log("Not Equal: " + (j != k));
console.log("Strict Not Equal: " + (j !== k));
console.log("Greater Than: " + (j > k));
console.log("Less Than: " + (j < k));
console.log("Greater Than or Equal: " + (j >= k));
console.log("Less Than or Equal: " + (j <= k));

// Logical Operators
// &&, ||, !
let l = true;
let m = false;
console.log("Logical AND: " + (l && m));
console.log("Logical OR: " + (l || m));
console.log("Logical NOT: " + (!l));

// Bitwise Operators
// &, |, ^, ~, <<, >>
let n = 10; // 1010
let o = 4; // 0100
console.log("Bitwise AND: " + (n & o)); // 0000
console.log("Bitwise OR: " + (n | o)); // 1110
console.log("Bitwise XOR: " + (n ^ o)); // 1110
console.log("Bitwise NOT: " + (~n)); // 0101
console.log("Left Shift: " + (n << 1)); // 10100
console.log("Right Shift: " + (n >> 1)); // 0101

// Ternary Operator
// condition ? expr1 : expr2
